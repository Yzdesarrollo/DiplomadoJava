package Agenda;

public class Contactos {
	
	private String nombre;
	private String numero;
	
	public Contactos() {
		
	}
	
	public String getNombre()
	{
		return nombre;
	}
	
	public String getNumero()
	{
		return numero;
	}
	
	public void setNombre(String nombre)
	{
		this.nombre = nombre;
	}
	
	public void setNumero(String numero)
	{
		this.numero = numero;
	}

}
